"""@package docstring
Classes for all melee weapons, including knives and spears.

Weapons that can be used both in melee and be thrown are classed as ComboWeapons, defined in ThrownWeap.py.
"""


import Weap


class MeleeWeap (Weap):
    pass


class Axe (MeleeWeap):
    pass


class Sword (MeleeWeap):
    pass


class Knife (MeleeWeap):
    pass


class Spear (MeleeWeap):
    pass


class Polearm (MeleeWeap):
    pass
